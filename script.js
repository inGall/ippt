function calculate() {
  var selectedPushUpReps = document.getElementById("pushupGroup");
  var pushUpReps = 60 - parseInt(selectedPushUpReps.options[selectedPushUpReps.selectedIndex].value);

  var selectedSitUpReps = document.getElementById("situpGroup");
  var sitUpReps = 60 - parseInt(selectedSitUpReps.options[selectedSitUpReps.selectedIndex].value);

  var selectedRunTime = document.getElementById("runGroup");
  var runTime = 60 - parseInt(selectedRunTime.options[selectedRunTime.selectedIndex].value);

  var selectedAgeGroup = document.getElementById("ageGroup");
  var ageGroup = parseInt(selectedAgeGroup.options[selectedAgeGroup.selectedIndex].value);

  document.getElementById("pushupPoints").value = pushupTable[pushUpReps][ageGroup];
  document.getElementById("situpPoints").value = situpTable[sitUpReps][ageGroup];
  document.getElementById("runPoints").value = runTable[runTime][ageGroup];

  var totalPoints = pushupTable[pushUpReps][ageGroup] + situpTable[sitUpReps][ageGroup] + runTable[runTime][ageGroup];
  
  document.getElementById("totalPoints").innerHTML = totalPoints;
  document.getElementById("award").innerHTML = getAward(totalPoints);
}

function getAward(totalPoints) {
  var selectedVocation = document.getElementById("vocationGroup");
  var vocation = selectedVocation.options[selectedVocation.selectedIndex].value;
  var award;

  if(vocation === "specialUnit") {
    if(totalPoints >= 90) {
      award = "Gold";
    } else if (totalPoints >= 75) {
      award = "Silver";
    } 
  }

  if(vocation === "normalUnit") {
    if(totalPoints >= 85) {
      award = "Gold";
    } else if(totalPoints >= 75) {
      award = "Silver";
    }
  }

  if(totalPoints < 51) {
    award = "Fail";
  } else if (totalPoints < 61) {
    award = "Pass";
  } else if (totalPoints <= 74) {
    award = "Pass with Incentive";
  }

  return award;
}
